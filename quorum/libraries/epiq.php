    <h1>Educational Information</h1>
    <h4>What do we learn at EPIQ?</h4>
    <p align="justify">During the week, you will learn computer programming while working through the 
        student curriculum. You will be provided direct instruction in the 
        programming activities and will also be given 
        resources to implement the curriculum with your students.</p>
    
    <h4>I went to EPIQ 2014. What new things will we learn this year? </h4>
    <p align="justify">We have a variety of new things planned, including details
    on the new Quorum 3 compiler and new versions of the programming environments. 
    Besides this, we have a number of not-yet-public features planned for release, which
    we hope to announce and teach at the workshop. To say the least, this will be
    an exciting and new year of EPIQ.</p>

    <h4>What kind of students can learn programming?</h4>
    <p align="justify"> We believe that all students can learn to program. This 
        program is accessible to all students. Quorum has been designed and evaluated in 
        <a href="http://dl.acm.org/citation.cfm?id=2534973">scientific, peer-reviewed, studies</a>
        , providing evidence of its ease of use. Given it is also based on the 
        commercial-scale Java Virtual Machine, a wide variety of commercial applications
        can be built with the technology (e.g., games, websites). Quorum
        also has built-in accessibility for students with disabilities,
        particularly those with visual impairments and 
        blindness.</p>


    <h1>Travel Information</h1>
    <h4>Where Is EPIQ held?</h4>
    <p align="justify"> The EPIQ Workshop is held on the campus of the Washington State 
        School for the Blind in Vancouver, WA.</p>



    <h4>When is EPIQ held?</h4>
    <p align="justify">For next year's EPIQ, the dates are July 22-29, 2015.</p>



    <h4>How much does it cost?</h4>
    <p align="justify"></p>
    We work to accrue external funding to make it possible
    for teachers to come and learn for free. Corporate sponsors that want to
    help, should contact the organizers.


    <h4>Where do we stay and how much does it cost?</h4>
    <p align="justify"></p>
    Free housing is provided on the campus of the Washington State School for the 
    Blind. Housing is “dorm-style” with each attendee having their own room.  The 
    “cottages” have a full kitchen, washer/dryer, and living room. Internet is 
    provided for free throughout the campus.  However, there are hotels in the area, 
    if you would prefer to stay off-campus at your own expense. We do not have
    unlimited space in our free housing, so we would encourage anyone wanting
    to attend to apply shortly after the application comes out next year.

    <h4>What about food?</h4>
    <p align="justify">Breakfast and lunch are provided by the WSSB and provided in the school’s 
        cafeteria. The food is not your “typical” school food and special diets 
        are accommodated.</p>


    <h4>What about travel to Vancouver, WA?</h4>
    <p align="justify">Vancouver, WA is on the border with Oregon and is “across the river” 
        from Portland. Air travel is easy in and out of the Portland International 
        Airport. At this time, we are securing funds to provide a select number of 
        applicants with stipends to help cover travel costs. However, stipends are
        not unlimited and we will base the number we provide on available funding.</p>


    <h4>What about travel around Vancouver/Portland?</h4>
    <p align="justify">Transportation to and from the airport will be provided by the workshop. The 
        workshop organizers will attempt to provide transportation around the area for 
        dinners, trips to the stores, etc. However, individuals are welcome to rent a 
        car or drive their own cars. Free parking is available.</p>


    <h1>Other EPIQ Questions</h1>
    <h4>What will we do all day?</h4>
    <p align="justify">You will work with leading professionals on learning how to program in Quorum 
        while working through the curriculum that you would use with your students. 
        Your presenters will include the inventor of the language, Dr. Andreas Stefik, 
        along with other leading professionals. Days typically start around 8:00am 
        (breakfast) and end around 4:00pm each day.  We provide ample breaks and a sizable 
        lunch break. </p>



    <h4>What will we do in the evenings?</h4>
    <p align="justify">That is your choice!  You can plan a trip with fellow travelers and go 
        see the local sights.  Or, go explore the local cuisine in the Vancouver/Portland 
        area.  Or, order a pizza, sit in the living room, and relax with 
        friends. Portland is a cool town, so lots of potential activities are
        available.
    </p>

    <h4>Do we do anything together for fun? </h4>
    <p align="justify">We are still in the planning stages for activites for EPIQ 2015  </p>



