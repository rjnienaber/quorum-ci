<h4>What platforms does Quorum support?</h4>
<p align="justify">
    Quorum can be run on most platforms that support the Java Virtual Machine. We have
    run Quorum on various versions of Windows, Mac, and Linux. 
</p>
<h4>How can I contribute to the Quorum project?</h4>
<p align="justify">
    There are many ways to contribute. If you have technical skills
    and want to contribute to the standard library, you can either make a pull request
    or get in touch with our development team. If you prefer to make a <strong>tax-exempt</strong>
    donation to the Quorum project, you can do so through the Quorum Outreach and Research
    Foundation by getting in touch with the development team. 
    An easy way to make a financial contributions online is in the works.
</p>
<h4>Can I attend the annual Quorum conference (Experience Programming in Quorum)?</h4>
<p align="justify">
    We do hold an annual conference on Quorum. While we do accept applications to
    attend, usually in March, this conference is invitation only. If we receive 
    a sufficient number of requests, we may eventually open the conference up to
    the general public.
</p>
