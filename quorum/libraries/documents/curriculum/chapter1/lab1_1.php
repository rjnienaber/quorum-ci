<h2>Overview</h2>
<p>
The goal of this lab is to become familiar with the Sodbeans integrated development environment (IDE) before jumping into the Quorum programming language. This lab will guide you through the tutorial and navigation systems, as well as give you an understanding of projects and files and how they work in the IDE. It is important to understand how to use the tutorials that are provided with Sodbeans, navigate around the environment, and work with projects and files.
</p>
<h2>Task 1: Using the Tutorial System</h2>
<p>
The tutorial system in Sodbeans is a tool that provides a basic understanding of the Sodbeans development environment and the basics of the Quorum programming language.  You will begin by first opening the Sodbeans program.  Open the Sodbeans program. After some waiting, the main Sodbeans window will appear. When Sodbeans has been loaded successfully, the first thing to notice is the Sodbeans Tutorials window.
</p>
<p>
The tutorial window consists of six different options to choose from.  The six choices are:  1) Hotkeys Reference, 2) Sodbeans Tutorials, 3) Quorum Tutorials, 4) Quorum Standard Library, 5) Navigation Tutorial, and 6) Close Tutorials Window.  Use the keyboard to press any numbers from one to six or click on the one of six buttons to make a selection. For this lab, you will visit the Hotkeys Reference and Sodbeans Tutorials.
</p>
<p>
First, visit the Hotkeys Reference to get a general overview of the hotkeys one might use in Sodbeans.  Press 1 on the keyboard, or click on the button labeled ![1], to enter the Hotkeys Reference window.  This window displays a list of every hotkey, along with the action that they perform. This reference is helfpul, as there is no need to memorize every hotkey to complete a task. Over time, commonly used hotkeys will be easily memorized and will help you work more efficiently. Take a look through the list and write down or type the keyboard shortcuts, along with the description for each shortcut listed below.
