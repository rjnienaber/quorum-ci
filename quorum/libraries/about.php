<h4>What kind of programming language is Quorum?</h4>
<p align="justify">
    Quorum is a general purpose tool that we call an "evidence-based" 
    programming language. It started as an interpreted
    language originally designed to be easier to hear through screen readers
    for blind or visually impaired users. Eventually, Quorum became a general
    purpose programming language designed for any user. Current versions compile to 
    Java Bytecode and run on the Java Virtual Machine, similarly to JRuby, Jython, or Scala. 
    Quorum 3.0 also compiles to JavaScript and can be run from the web. 
</p>
<h4>What are the high level technical details of Quorum?</h4>
<p align="justify">
    Quorum has many features. It is object-oriented, but this is largely hidden
    from new users. It has a general purpose type system, with generics for 
    containers (e.g., arrays, hash tables, lists). Quorum also has a 
    standard library, which contains many additions to the language, like
    math libraries, containers, web components, or other features.
</p>

<h4>How is Quorum funded?</h4>
<p align="justify">
    Quorum's initial funding started as part of a National Science Foundation 
    project (NSF CNS-0940521). Current funding for Quorum also comes from the 
    National Science Foundation (NSF CNS-1440878). Other funders include the Washington
    State School for the Blind, Southern Illinois University Edwardsville, the 
    University of Nevada, Las Vegas, the Do it program at the University of Washington, 
    amongst others.
</p>
